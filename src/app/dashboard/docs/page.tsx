// ── Общие блоки оформления ────────────────────────────────────────────────────

function Doc({ children }: { children: React.ReactNode }) {
  return (
    <div className="bg-white border border-slate-200 rounded-2xl p-6 space-y-6 text-sm text-slate-700 leading-relaxed">
      {children}
    </div>
  );
}

function Section({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <div>
      <h3 className="font-bold text-slate-900 mb-1.5">{title}</h3>
      {children}
    </div>
  );
}

function Pre({ children }: { children: string }) {
  return (
    <pre className="mt-2 bg-slate-50 border border-slate-200 rounded-lg p-3 text-xs font-mono whitespace-pre-wrap">{children}</pre>
  );
}

function Code({ children }: { children: string }) {
  return (
    <code className="bg-slate-100 border border-slate-200 rounded px-1.5 py-0.5 font-mono text-xs">{children}</code>
  );
}

function Muted({ children }: { children: React.ReactNode }) {
  return <p className="mt-2 text-slate-500">{children}</p>;
}

